function getVehiclePaintjobs(vehicle)
    if not vehicle then
        return false
    end

    local modelID = getElementModel(vehicle)
    if paintjobs[modelID] then
        return #paintjobs[modelID]
    end
    return 0
end


function getModelPaintjobs(modelID)
    if not modelID then
        return false
    end

    if paintjobs[modelID] then
        return paintjobs[modelID]
    end
    return false
end