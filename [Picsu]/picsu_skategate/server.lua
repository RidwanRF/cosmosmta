addCommandHandler("skategate",
    function(player)
		if getElementData(player, "acc.adminLevel") >= 10 then
			for i,v in pairs(getElementsByType("object")) do
				if getElementData(v,"skate.object") then
					local x, y, z = getElementPosition(v)
						if not getElementData(v,"skate.objectlent") then
							moveObject ( v, 3000, x,y,z-1.3)
							setTimer(function()
								setElementData(v, "skate.objectlent", true)
							end, 3000, 1 )
						else
							moveObject ( v, 3000, x,y,z+1.3)
							setTimer(function()
								setElementData(v, "skate.objectlent", false)
							end, 3000, 1 )
						end
				end	
			end	
		end
    end
)